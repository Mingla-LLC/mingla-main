-- C2: Rewrite phone invite trigger to ALSO convert pending_session_invites
-- When a non-app user signs up and verifies their phone, we must:
--   1. Convert pending_invites (friend invites) — already done
--   2. Convert pending_session_invites — NEW: create collaboration_invites rows
