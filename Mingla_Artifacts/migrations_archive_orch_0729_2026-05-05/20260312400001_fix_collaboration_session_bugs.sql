-- Drop the old constraint (name auto-generated by CREATE TABLE ... UNIQUE)
ALTER TABLE public.pending_session_invites
  DROP CONSTRAINT IF EXISTS pending_session_invites_session_id_phone_e164_key;

-- Add the wider constraint
ALTER TABLE public.pending_session_invites
  ADD CONSTRAINT pending_session_invites_session_inviter_phone_key
  UNIQUE (session_id, inviter_id, phone_e164);


-- ═══════════════════════════════════════════════════════════
-- H1: Prevent concurrent board creation race condition
-- Add a UNIQUE partial index on collaboration_sessions.board_id (non-null only).
-- This ensures only one session can be assigned a given board, and more importantly,
-- the second concurrent UPDATE that tries to set board_id will fail rather than
-- silently overwrite. Combined with client-side .eq("status", "pending") optimistic
-- locking, this makes board creation fully safe.
-- ═══════════════════════════════════════════════════════════

CREATE UNIQUE INDEX IF NOT EXISTS idx_collaboration_sessions_unique_board
  ON public.collaboration_sessions (board_id)
  WHERE board_id IS NOT NULL;


