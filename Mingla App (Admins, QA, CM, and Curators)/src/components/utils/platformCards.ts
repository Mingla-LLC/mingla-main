/**
 * Get all live cards (what explorers see)
 */
export function getLiveCards(): PlatformCard[] {
  return PLATFORM_CARDS_SEED.filter(card => card.status === 'live');
}

/**
 * Get cards by creator
 */
export function getCardsByCreator(creatorId: string): PlatformCard[] {
  return PLATFORM_CARDS_SEED.filter(card => card.createdBy === creatorId);
}

/**
 * Get cards by business
 */
export function getCardsByBusiness(businessId: string): PlatformCard[] {
  return PLATFORM_CARDS_SEED.filter(card => card.businessId === businessId);
}

/**
 * Get cards by status
 */
export function getCardsByStatus(status: PlatformCard['status']): PlatformCard[] {
  return PLATFORM_CARDS_SEED.filter(card => card.status === status);
}

/**
 * Get cards by curator (for curator dashboard)
 */
export function getCardsByCurator(curatorId: string): PlatformCard[] {
  return PLATFORM_CARDS_SEED.filter(card => 
    card.createdBy === curatorId || card.curatorId === curatorId
  );
}
