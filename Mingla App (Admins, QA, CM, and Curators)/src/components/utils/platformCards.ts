/**
 * Platform Cards - Centralized card data for all user roles
 * 
 * All live cards shown to explorers come from this centralized store.
 * Cards can be created by:
 * 1. Curators for the platform
 * 2. Businesses for the platform
 * 3. Curators for businesses (on behalf of businesses)
 */

export interface PlatformCard {
  id: string;
  title: string;
  category: string;
  description: string;
  image: string;
  images?: string[];
  location: string;
  venue: string;
  price: string;
  priceRange?: string;
  duration: string;
  rating: number;
  reviews: number;
  matchScore?: number;
  tags?: string[];
  highlights?: string[];
  
  // Card Status & Workflow
  status: 'draft' | 'in-review' | 'live' | 'returned';
  
  // Ownership & Attribution
  createdBy: string; // User ID of creator
  createdByRole: 'curator' | 'business' | 'qa' | 'api';
  createdByName: string;
  businessId?: string; // If created for a specific business
  businessName?: string;
  curatorId?: string; // If created by curator for a business
  isApiGenerated?: boolean; // If generated by AI from explorer query
  
  // Timestamps
  createdAt: string;
  lastEdited: string;
  publishedAt?: string;
  
  // Stats (for live cards)
  views?: number;
  likes?: number;
  saves?: number;
  shares?: number;
  totalSales?: number;
  
  // QA moderation
  feedback?: string;
  reviewedBy?: string;
  reviewedAt?: string;
  flaggedForReview?: boolean;
  flagReason?: string;
  flaggedBy?: string;
  flaggedAt?: string;
  
  // Additional metadata
  availability?: any;
  packages?: any[];
  policies?: any;
  routeSteps?: any[];
  totalDuration?: number;
}

